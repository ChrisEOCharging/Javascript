let starRating = 6;

switch(starRating){
    case 1:
        console.log('Very Bad');
        break;
    case 2:
        console.log('Bad');
        break;
    case 3:
        console.log('Average');
        break;
    case 4:
        console.log('Good');
        break;
    case 5:
        console.log('Excellent');
        break;
    default:
        console.log('Please enter value between 1 and 5');
}

